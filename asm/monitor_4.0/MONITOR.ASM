;WHEN CALLED BY WH@,WAITS FOR VALID DATA FLG IN KBUFF,
;THEN RETURNS CHAR FROM KBUFF+1 IN A.
KI:	PUSH	H	;WE CAN ONLY WRECK A, SO SAVE H
	LXI	H,KBUFF
KI1:	MOV	A,M	;GET FLAG
	ORA	A	;IS IT ZERO?
	JNZ	KI1	;NO,TRY AGAIN
	DCR	M
	INX	H
	MOV	A,M
	POP	H
	RET
USRTI:	PUSH	H
	LXI	H,RBUFF
